/// A namespace for all static image asset names used in the app.
///
/// This enum provides a centralized access point for image resources,
/// improving discoverability and minimizing typos.
///
/// Example usage:
/// ```swift
/// Image(ImageResources.dinoPlaceholder)
/// ```
enum ImageResources {
    
    static let dinoPlaceholder = "dino-placeholder"
    static let sadDino         = "sad-dino"
    
}
